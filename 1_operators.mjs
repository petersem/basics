// Assignment operators
let a = 1; // simple assign
a += 2; // same as a = a + 2
a -= 2; // same as a = a - 2
a *= 2; // same as a = a * 2
a /= 2; // same as a = a / 2
a **= 3; // same as a = a * a * (exponent)
a %= 2; // same as a = a % 2  (modulus)

