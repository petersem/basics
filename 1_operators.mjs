// Assignment of value
let a = 1; 

// Arithmatic operators
a = a + 1; // add
a = a - 1; // subtract
a = a * 2; // multiply
a = 20 / 10; // divide
a = 5 ** 2; // exponent
a = 10 % 2; // modulus (division remainder)
let b = ++a // increment by 1 before assignment
b = a++ // increment by 1 after assignment
b = --a // decrement by 1 before assignment
b = a-- // decrement by 1 after assignment

